// This program calculates and displays the area and
// perimeter of a rectangle with dimensions 6cm X 8cm.
#include <iostream>

int main() {
    std::cout << "For a rectangle that has dimensions\n";
    std::cout << "length 6cm and width 8cm:\n";
    std::cout << "\n";
    std::cout << "Area = " << 6 * 8 << " cm^2\n";
    std::cout << "Perimeter = " << 2 * (6 + 8) << " cm\n";
}
